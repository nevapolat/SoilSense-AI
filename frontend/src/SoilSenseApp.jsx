function coordsKey(c) {
  // Small rounding so repeated measurements don't re-trigger AI calls constantly.
  if (!c || typeof c.latitude !== 'number' || typeof c.longitude !== 'number') return 'invalid'
  if (!Number.isFinite(c.latitude) || !Number.isFinite(c.longitude)) return 'invalid'
  return `${c.latitude.toFixed(3)},${c.longitude.toFixed(3)}`
}

function toFixedOrDash(n, digits) {
  if (typeof n !== 'number' || Number.isNaN(n)) return '—'
  return n.toFixed(digits)
}

/**
 * When daily regenerative tasks are marked done, add a bounded boost to soil health (and a smaller
 * one to sustainability). Weighted by estimated minutes and step count as a proxy for effort / impact.
 */
function computeDailyTasksScoreImpact(tasks, completedIds) {
  if (!Array.isArray(tasks) || !tasks.length || !Array.isArray(completedIds)) {
    return { soilDelta: 0, sustainDelta: 0, completedCount: 0 }
  }
  const done = new Set(completedIds.map(String))
  let raw = 0
  let completedCount = 0
  for (const task of tasks) {
    if (!task || task.id == null) continue
    if (!done.has(String(task.id))) continue
    completedCount += 1
    const minutes =
      typeof task.estimatedMinutes === 'number' &&
      Number.isFinite(task.estimatedMinutes) &&
      task.estimatedMinutes > 0
        ? task.estimatedMinutes
        : 20
    const stepsN = Array.isArray(task.steps) ? task.steps.length : 0
    // Clamp inputs so score changes stay gradual even if daily tasks are regenerated by AI.
    const minutesClamped = Math.max(10, Math.min(35, minutes))
    const stepsClamped = Math.max(0, Math.min(4, stepsN))
    const effort = 0.55 + 0.45 * Math.min(1, minutesClamped / 55)
    const depth = 1 + Math.min(0.5, stepsClamped * 0.065)
    // Keep soil-score improvements gradual: reduce the per-day boost and cap the output.
    raw += 1.15 * effort * depth
  }
  const soilDelta = Math.min(6, raw)
  const sustainDelta = Math.min(3.25, raw * 0.54)
  return { soilDelta, sustainDelta, completedCount }
}

// Soil score should not behave like a pure “today weather” score.
// This compresses the weather-driven base so places like Istanbul/Kağıthane
// (humid + rainy) don’t automatically produce unrealistically high soil health.
function capWeatherDrivenSoilHealthScore(rawScore) {
  if (typeof rawScore !== 'number') return rawScore
  // JSON may deserialize invalid numbers; never propagate NaN/Infinity into the UI.
  if (!Number.isFinite(rawScore)) return 55
  const x = Math.max(0, Math.min(100, rawScore))
  // Maps raw score -> conservative ceiling.
  // Example: raw 72 -> cap ~56; raw 85 -> cap ~59; raw 100 -> cap ~63.
  const capped = 50 + (x - 50) * 0.25
  return Math.max(0, Math.min(100, Math.round(capped)))
}

function parseNumericSoilScore(value) {
  if (typeof value === 'number' && Number.isFinite(value)) return value
  if (typeof value === 'string' && value.trim()) {
    const n = Number(String(value).trim().replace(',', '.'))
    if (Number.isFinite(n)) return n
  }
  return null
}

/** Weather-only score from LLM JSON or deterministic fallback when parse fails or score is missing/invalid. */
function resolveVitalityWeatherScore(vitalityJson, signals, lang) {
  const fallback = buildSoilVitalityScoreFallback(signals, lang)
  if (!vitalityJson || vitalityJson.parseError) {
    return { soilHealthScore: fallback.soilHealthScore, explanation: fallback.explanation, usedFallback: true }
  }
  const n = parseNumericSoilScore(vitalityJson.soilHealthScore)
  if (n != null) {
    return {
      soilHealthScore: n,
      explanation: typeof vitalityJson.explanation === 'string' ? vitalityJson.explanation : '',
      usedFallback: false,
    }
  }
  return { soilHealthScore: fallback.soilHealthScore, explanation: fallback.explanation, usedFallback: true }
}

function normalizeCustomEquipmentLabels(input) {
  if (!Array.isArray(input)) return []
  const seen = new Set()
  const out = []
  for (const x of input) {
    const s = typeof x === 'string' ? x.trim().slice(0, 48) : ''
    if (s.length < 1) continue
    const key = s.toLowerCase()
    if (seen.has(key)) continue
    seen.add(key)
    out.push(s)
    if (out.length >= 12) break
  }
  return out
}

